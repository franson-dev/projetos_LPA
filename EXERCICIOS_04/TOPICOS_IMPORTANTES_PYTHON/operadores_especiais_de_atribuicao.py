'''
    =============================================================================
    TABELA: OPERADORES DE ATRIBUIÇÃO COMPOSTA (ESPECIAIS)
    =============================================================================
    | Operador | Exemplo  | Equivalente a     | Descrição                       |
    |----------|----------|-------------------|---------------------------------|
    | +=       | x += 3   | x = x + 3         | Adição e atribuição             |
    | -=       | x -= 3   | x = x - 3         | Subtração e atribuição          |
    | *=       | x *= 3   | x = x * 3         | Multiplicação e atribuição      |
    | /=       | x /= 3   | x = x / 3         | Divisão e atribuição            |
    | %=       | x %= 3   | x = x % 3         | Módulo e atribuição             |
    | //=      | x //= 3  | x = x // 3        | Divisão inteira e atribuição    |
    | **=      | x **= 3  | x = x ** 3        | Exponenciação e atribuição      |
    | &=       | x &= 3   | x = x & 3         | AND bit a bit e atribuição      |
    | |=       | x |= 3   | x = x | 3         | OR bit a bit e atribuição       |
    | ^=       | x ^= 3   | x = x ^ 3         | XOR bit a bit e atribuição      |
    | >>=      | x >>= 3  | x = x >> 3        | Shift direita e atribuição      |
    | <<=      | x <<= 3  | x = x << 3        | Shift esquerda e atribuição     |
    =============================================================================
'''
